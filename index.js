function add(a, b) {
    return a + b
}

function makeInt(n) {
    return parseInt(n, 10)
}

function preserveDecimal(n) {
    return parseFloat(n)
}

function subtract(a, b) {
    return a - b
}

function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

function mod(a, b) {
    return a % b
}

function increment(n) {
    return n + 1
}

function decrement(n) {
    return n - 1
}